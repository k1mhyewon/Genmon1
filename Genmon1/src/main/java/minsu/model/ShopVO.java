package minsu.model;

public class ShopVO {
	
	
	private String sname;    // 매장이름
	private String simg;     // 매장사진
	private String sadr;     // 매장주소
	private String stel;      // 매장전화번호
	private String stime;     // 영업시간
	private String sdesc;     // 매장설명
	
	
	// ========================================================== //
	
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public String getSimg() {
		return simg;
	}
	public void setSimg(String simg) {
		this.simg = simg;
	}
	public String getSadr() {
		return sadr;
	}
	public void setSadr(String sadr) {
		this.sadr = sadr;
	}
	public String getStel() {
		return stel;
	}
	public void setStel(String stel) {
		this.stel = stel;
	}
	public String getStime() {
		return stime;
	}
	public void setStime(String stime) {
		this.stime = stime;
	}
	public String getSdesc() {
		return sdesc;
	}
	public void setSdesc(String sdesc) {
		this.sdesc = sdesc;
	}
	
	
	

}
